        # put r1, r2 into the integral basis (1, Ï‰)
        d = self.d
        omega  = RealQuadraticField(d).omega
        m  = gl2z.M2Z(self.r1.x, self.r2.x, self.r1.y, self.r2.y) # 2Ã—2 integer matrix
        _, H  = gl2z.hnf_2x2(m.transpose())            # any HNF routine you like
